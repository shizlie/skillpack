// scripts/build-dashboard.js
// Pre-bundles the dashboard browser entry point so the Worker can serve it
// as a static asset without calling Bun.build() at runtime.
//
// Output: apps/dashboard/src/ui/dist/dashboard.bundle.js
//   An ES module that exports the browser bundle text as its default export.
//   Importable by both Bun (native) and Cloudflare Workers (native ESM).
import { mkdir } from "node:fs/promises";
import { dirname } from "node:path";

const ENTRY = "apps/dashboard/src/ui/index.js";
const OUT = "apps/dashboard/src/ui/dist/dashboard.bundle.js";

const result = await Bun.build({
  entrypoints: [ENTRY],
  format: "esm",
  bundle: true,
  target: "browser",
  minify: false,
});

if (!result.success) {
  console.error("Build failed:", result.logs);
  process.exit(1);
}

const bundleText = await result.outputs[0].text();
// Wrap in a valid ES module so `import dashboardScript from "...bundle.js"` works
// natively in both Bun and Cloudflare Workers without any loader plugin.
const moduleContent =
  `// AUTO-GENERATED by scripts/build-dashboard.js — do not edit\n` +
  `export default ${JSON.stringify(bundleText)};\n`;

await mkdir(dirname(OUT), { recursive: true });
await Bun.write(OUT, moduleContent);
console.log(`Wrote ${OUT} (${moduleContent.length} bytes)`);
